'''
Ejercicio 1: Operadores Aritméticos

Problema: Escribe un programa que solicite al usuario dos números y 
realice las operaciones de suma, resta, multiplicación, división y módulo.

'''
numero_1=float (input ("favor digitar primer numero: "))
numero_2=float (input ("favor digitar segundo numero: : "))

#operador suma
print("La suma de los numeros es: ",numero_1+numero_2)

#operador resta
print("La resta de los numeros es: ",numero_1-numero_2)

#operador multiplicación
print("La multiplicación de los numeros es: ",numero_1*numero_2)

#operador Division
print("La división de los numeros es: ",numero_1/numero_2)

#operador modulo
print("El modulo de los numeros es: ",numero_1%numero_2)
